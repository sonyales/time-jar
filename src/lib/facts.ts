/**
 * getFactForPercent ищет факт, который подходит к текущему проценту.
 */
export function getFactForPercent(percent: number): Fact | null {
  /**
   * Округляем процент вниз.
   */
  const currentPercent = Math.floor(percent);

  /**
   * Фильтруем только те факты,
   * процент которых уже достигнут.
   */
  const availableFacts = facts.filter((fact) => fact.percent <= currentPercent);

  /**
   * Если подходящих фактов нет, возвращаем null.
   */
  if (availableFacts.length === 0) {
    return null;
  }

  /**
   * Берём последний подходящий факт.
   */
  return availableFacts[availableFacts.length - 1];
}